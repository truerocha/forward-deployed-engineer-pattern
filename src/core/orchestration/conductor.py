"""
Conductor — RL-Inspired Orchestration Engine for Agent Coordination.

Implements the Conductor pattern (Nielsen et al., ICLR 2026) adapted for
the FDE Code Factory. The Conductor is a lightweight meta-orchestrator that:

  1. Decomposes complex tasks into targeted subtasks
  2. Assigns subtasks to specialized agents with focused instructions
  3. Designs communication topologies (access lists) between agents
  4. Adapts workflow complexity to task difficulty (organism level)
  5. Supports recursive self-referential scaling for hard problems

Key insight from the paper: "A 7B Conductor achieves significant performance
gains beyond any individual worker by learning optimal coordination strategies."

Our adaptation maps this to the FDE distributed execution model:
  - Conductor = enhanced orchestrator that generates WorkflowPlans
  - Workers = ECS agent tasks (parametrized by role + model tier)
  - Communication topology = SCD section access permissions
  - Recursive scaling = re-entry into planning when confidence is low
  - Task adaptivity = organism ladder drives workflow complexity

Architecture:
  - Receives task + organism classification + knowledge context
  - Generates a WorkflowPlan (steps, assignments, access topology)
  - Passes plan to DistributedOrchestrator for execution
  - Monitors results and triggers recursive refinement if needed

Design decisions:
  - Conductor uses Bedrock (reasoning tier) for plan generation
  - Plans are deterministic once generated (no runtime re-planning mid-stage)
  - Recursive depth is bounded (max 2 levels) to prevent runaway cost
  - Communication topology is expressed as access lists per step
  - Task difficulty adaptivity uses organism level as primary signal

Ref: Nielsen et al. "Learning to Orchestrate Agents in Natural Language
     with the Conductor" ICLR 2026 (arXiv:2512.04388v5)
Ref: docs/design/fde-core-brain-development.md Section 3.1
Ref: docs/example/FDE design pattern/Indice Mestre (Parte 4: Orquestracao)
"""

from __future__ import annotations

import json
import logging
import time
from dataclasses import dataclass, field
from enum import Enum
from typing import Any

import boto3
from botocore.exceptions import ClientError

logger = logging.getLogger(__name__)


# --- Workflow Plan Data Structures ---


class TopologyType(Enum):
    """Communication topology types between agents."""

    SEQUENTIAL = "sequential"
    PARALLEL = "parallel"
    TREE = "tree"
    DEBATE = "debate"
    RECURSIVE = "recursive"


@dataclass
class WorkflowStep:
    """A single step in the Conductor's workflow plan.

    Maps to the Conductor paper's output format:
      - subtask: natural language instruction for the agent
      - agent_id: which agent role handles this step
      - access_list: which previous step outputs are visible

    The access_list implements the communication topology:
      - [] = no visibility to previous outputs (independent)
      - ["all"] = sees all previous step outputs
      - [0, 2] = sees outputs from steps 0 and 2 only
    """

    step_index: int
    subtask: str
    agent_role: str
    model_tier: str
    access_list: list = field(default_factory=list)
    timeout_seconds: int = 600
    retry_max: int = 3

    def has_full_access(self) -> bool:
        """Whether this step sees all previous outputs."""
        return "all" in self.access_list

    def visible_steps(self, total_steps: int) -> list[int]:
        """Resolve access list to concrete step indices."""
        if "all" in self.access_list:
            return list(range(self.step_index))
        return [i for i in self.access_list if isinstance(i, int) and i < self.step_index]


@dataclass
class WorkflowPlan:
    """Complete workflow plan generated by the Conductor.

    Represents the full coordination strategy for a task:
      - Steps define what each agent does
      - Topology defines how agents communicate
      - Metadata tracks planning decisions for observability
    """

    task_id: str
    organism_level: str
    topology_type: TopologyType
    steps: list[WorkflowStep] = field(default_factory=list)
    recursive_depth: int = 0
    confidence_threshold: float = 0.7
    planning_rationale: str = ""
    estimated_tokens: int = 0
    metadata: dict[str, Any] = field(default_factory=dict)

    def total_steps(self) -> int:
        return len(self.steps)

    def agent_roles(self) -> list[str]:
        return list(dict.fromkeys(s.agent_role for s in self.steps))

    def model_tiers_used(self) -> dict[str, int]:
        counts: dict[str, int] = {}
        for step in self.steps:
            counts[step.model_tier] = counts.get(step.model_tier, 0) + 1
        return counts

    def to_squad_manifest_stages(self) -> dict[int, list[dict[str, Any]]]:
        """Convert workflow plan to SquadManifest stage format."""
        if self.topology_type == TopologyType.PARALLEL:
            return {1: [self._step_to_agent_spec(s) for s in self.steps]}
        elif self.topology_type == TopologyType.DEBATE:
            attempts = self.steps[:-1]
            arbiter = self.steps[-1]
            return {
                1: [self._step_to_agent_spec(s) for s in attempts],
                2: [self._step_to_agent_spec(arbiter)],
            }
        else:
            stages: dict[int, list[dict[str, Any]]] = {}
            for i, step in enumerate(self.steps):
                stages[i + 1] = [self._step_to_agent_spec(step)]
            return stages

    def _step_to_agent_spec(self, step: WorkflowStep) -> dict[str, Any]:
        return {
            "role": step.agent_role,
            "model_tier": step.model_tier,
            "stage": step.step_index + 1,
            "subtask": step.subtask,
            "access_list": step.access_list,
            "timeout_seconds": step.timeout_seconds,
            "retry_max": step.retry_max,
        }


